        <h3 class="tc b">如何进行实名认证</h3><hr />
        <p> 当您使用小鹅通个人模式时，您销售内容商品的货款将由小鹅通代收，您可通过发起提现申请将所得收入提现至已绑定的提现微信号。</p>
