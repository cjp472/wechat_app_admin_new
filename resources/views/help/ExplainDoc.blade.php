        <h3>具体操作方式如下：</h3>
        <p>Step1：登录您的<span style="color: red">手机微信客户端</span>，点击最下面菜单栏“<span style="color: red">我</span>”的选项，找到“<span style="color: red">钱包</span>”选项。</p>
        <img style="width: 330px;height: 590px;margin-left: 340px;" src="../images/help/step1.png" alt=""/>
        <p>Step2：点击“钱包”选项，进入钱包页面，点击右上角“更多”选项（图标为<span style="color: red">三个圆点</span>）。</p>
        <img style="width: 330px;height: 590px;margin-left: 340px;" src="../images/help/step2.png" alt=""/>
        <p>Step3：点击“<span style="color: red">支付管理</span>”选项。</p>
        <img style="width: 330px;height: 590px;margin-left: 340px;" src="../images/help/step3.png" alt=""/>
        <p>Step4：在支付管理页面查看您的账户是否已经实名认证。</p>
        <p>如果在“实名认证”栏显示您“<span style="color: red">已认证</span>”，那么您可以回到小鹅通管理后台，继续进行提现操作，当日提现金额可以超过2000元，但不超过2万元。</p>
